struct resource_region {
	u32_t	ioaddr;
	u32_t	size;
	char	*name;	
	caddr_t	addr;
};
#define MAX_RR	64
struct resource_region _RR[MAX_RR];

int
request_region(u32_t ioaddr, u32_t n,char *name)
{
	int	i;
	caddr_t	vaddr;

	for(i=0; i<MAX_RR; i++) 
		if (_RR[i].ioaddr == 0) break;
	if (i==MAX_RR) {
		printf("MAX Resources RR reached for PCI\n");
		return 0;
	}
	if (!(vaddr = (caddr_t)physmap(ioaddr,n,KM_NOSLEEP))) {
		return 0;
	}

	_RR[i].ioaddr = ioaddr;
	_RR[i].size = n;
	_RR[i].name = name;
	_RR[i].addr = vaddr;
	return 1;
}

int
release_region(u32_t ioaddr,u32_t n)
{
	int	i;
	
	for(i=0; i<MAX_RR; i++) {
		if (_RR[i].ioaddr == ioaddr) {
			_RR[i].ioaddr = 0;
			physmap_free(_RR[i].addr,_RR[i].size,KM_NOSLEEP);
			_RR[i].name = 0;
			return 1;
		}
	}
	return 0;
}
/*
 * pic_add_handler() - update PIC masks after adding a new IRQ handler
 * must be called at spl7() with interrupts disabled
 */
void
pic_add_handler()
{
	int 	mask,intno, bit, slaves, level, pic;

	/*
	 * if picinit() has not yet run skip the hw update
	 * picinit() will pick this up when it runs
	 */
	if (!splvalid_flag)
		return;

	for (pic=0; pic<npic; pic++) {
		mask=0;
		intno = pic * 8;
		for(bit=1; bit<=0x80; bit<<=1, intno++) {
			if (intno >= nintr || ivect[intno] == intnull) {
				mask |= bit;
				for(level=SPLTTY; level<IPLHI; level++)
					iplmask[level * npic+pic] |= bit;
			} else {
				level = intpri[intno];
				for(; level<IPLHI; level++)
					iplmask[level * npic+pic] |= bit;
			}
		}
		iplmask[pic]=mask;
	}

	/* Re-enable slave cascade lines on master */
	slaves = 0;	
	for(pic=1; pic<npic; pic++)
		slaves |= 1 << masterline[pic];
	iplmask[0] &= ~slaves;

	/* Write new OCW1 masks only - no ICW sequence */
	for(pic=0; pic<npic; pic++)
		outb(imrport[pic], (curmask[pic] = iplmask[pic]));
}


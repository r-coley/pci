void
pciinit()
{
	uint_t	tmp;
	int	x;
	extern int verbose_bios;

	DrvDebug(_PCI,5,"pciinit()\n");
	if (pci_debug>0)
		verbose_bios=1;

	if (pci_devices != 0) return;

	bzero(_RR,sizeof(_RR));

	outl(CFG_ADDR,0);
	tmp=inl(CFG_ADDR);
	if (tmp != 0x00000000)
	{
		printf("No PCI devices found\n");
		return;
	}

		pci_set_router(&pci_generic_router_ops);
	scan_bios();

	bzero(&pci_root, sizeof(pci_root));
	pci_root.subordinate = scan_bus(&pci_root);

	if (elcr_probe() != 0)
		printf("PCI: ELCR not found, level-triggered IRQs not available\n");

	/* Register the chipset driver */
	(void)I440FX_init();
	(void)PIIX3_init();
	(void)PIIX4_init();
	(void)VIA_init();

	/*show_pirq();
	show_irqroute();*/
	return; 
}


void
pciinit()
{
	uint_t	tmp;
	int	x;

	if (pci_devices != 0) return;
	outl(CFG_ADDR,0);
	tmp=inl(CFG_ADDR);
	if (tmp != 0x00000000)
	{
		printf("No PCI devices found\n");
		return;
	}

	PirRoute=&PCI_route;
	scan_bios();

	bzero(&pci_root, sizeof(pci_root));
	pci_root.subordinate = scan_bus(&pci_root);

	if (elcr_probe() != 0)
		printf("PCI: ELCR not found, level-triggered IRQs not available\n");

	/* Register the chipset driver */
	x=0;
	if (x == 0)
		x=I440FX_init();
	if (x == 0)
		x=PIIX3_init();
	if (x == 0)
		x=PIIX4_init();
	if (x == 0)
		x=VIA_init();

	/*show_pirq();
	show_irqroute();*/
	return; 
}


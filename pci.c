void
pci_buf_printf(char *buf, int maxlen, int *len, const char *fmt, ...)
{
	va_list ap;
	char tmp[256];
	int n;
	char *p;

	if (*len >= maxlen - 1)
		return;

	va_start(ap, fmt);
	kv_vsprintf(tmp, fmt, ap);
	va_end(ap);

	n = strlen(tmp);
	if (n <= 0)
		return;

	if (*len + n >= maxlen)
		n = (maxlen - 1) - *len;

	for (p = tmp; n-- > 0; p++)
		buf[(*len)++] = *p;

	buf[*len] = 0;
}

char
pci_intpin_name(u8_t pin)
{
	switch (pin) {
	case 1: return 'A';
	case 2: return 'B';
	case 3: return 'C';
	case 4: return 'D';
	}
	return '-';
}

char *
pci_display_name(pcidev_t *dev)
{
	u8_t base, sub;
	char *name;

#if 0
	if (dev->name != 0 && *dev->name != 0)
		return dev->name;
#endif

	base = (u8_t)((dev->class >> 16) & 0xff);
	sub  = (u8_t)((dev->class >> 8) & 0xff);

	name = get_pci_subclass_name(base, sub);
	if (name != 0)
		return name;

	name = get_pci_class_name(base);
	if (name != 0)
		return name;

	return "Unknown PCI Device";
}

void
pci_format_one_bar(resource_t *r, int barno, char *buf, int maxlen, int *len, int indent)
{
	char info[64];

	info[0] = 0;
	if (r->flags & IORESOURCE_IO) {
		strcpy(info, "IO ");
	} else if (r->flags & IORESOURCE_MEM) {
		strcpy(info, "MEM");
		if (r->flags & IORESOURCE_PREFETCH)
			strcat(info, " prefetch");
		if (r->flags & IORESOURCE_MEM_64)
			strcat(info, " 64-bit");
		else
			strcat(info, " 32-bit");
	} else {
		strcpy(info, "???");
	}

	pci_buf_printf(buf, maxlen, len,
	    "%*sBAR%d %-18s %08lx-%08lx\n",
	    indent, "",
	    barno,
	    info,
	    (unsigned long)r->start,
	    (unsigned long)r->end);
}

void
pci_format_bars(pcidev_t *dev, char *buf, int maxlen, int *len, int indent)
{
	int i;
	resource_t *r;

	for (i = 0; i < DEVICE_COUNT_RESOURCE; i++) {
		r = &dev->resource[i];
		if (r->flags == 0)
			continue;

		pci_format_one_bar(r, i, buf, maxlen, len, indent);
	}
}

void
pci_format_bus_tree(pcibus_t *bus, char *buf, int maxlen, int *len, int indent)
{
	pcidev_t *dev;

	for (dev = bus->devices; dev != 0; dev = dev->sibling) {
		pci_buf_printf(buf, maxlen, len,
		    "%*s%02x:%02x.%x %s [%04x:%04x] IRQ:%d INT#%c\n",
		    indent, "",
		    dev->bn,
		    PCI_SLOT(dev->devfn),
		    PCI_FUNC(dev->devfn),
		    pci_display_name(dev),
		    dev->vendor,
		    dev->device,
		    dev->irq,
		    pci_intpin_name(dev->intpin));

		pci_format_bars(dev, buf, maxlen, len, indent + 2);

		if (dev->child_bus != 0) {
			pci_buf_printf(buf, maxlen, len,
				"%*sBus %02x\n",
				indent+2,"",
				dev->child_bus->number);

			pci_format_bus_tree(dev->child_bus, buf, maxlen, len, indent + 2);
		}
	}
}

int
pci_format_snapshot(char *buf, int maxlen)
{
	int len = 0;
	int irq;

	if (maxlen <= 0)
		return 0;

	buf[0] = 0;
	pci_buf_printf(buf, maxlen, &len, "PCI device tree:\n");

	if (pci_router != 0) {
		pci_buf_printf(buf, maxlen, &len,
		    "Router: %s bus=%02x devfn=%02x base=0x%x\n",
		    pci_router->name ? pci_router->name : "(unknown)",
		    Pbn, Pdevfn, pci_router->pirq_base);

		if (pci_router->route != 0) {
			irq = pci_router->route(PIRQ_GET, 1);
			if (irq & 0x80)
				pci_buf_printf(buf, maxlen, &len, 
						"   PIRQA=disabled");
			else
				pci_buf_printf(buf, maxlen, &len, 
						"   PIRQA=%d", irq & 0x0f);
			irq = pci_router->route(PIRQ_GET, 2);
			if (irq & 0x80)
				pci_buf_printf(buf, maxlen, &len, 
						"   PIRQB=disabled");
			else
				pci_buf_printf(buf, maxlen, &len, 
						"   PIRQB=%d", irq & 0x0f);
			irq = pci_router->route(PIRQ_GET, 3);
			if (irq & 0x80)
				pci_buf_printf(buf, maxlen, &len, 
						"   PIRQC=disabled");
			else
				pci_buf_printf(buf, maxlen, &len, 
						"   PIRQC=%d", irq & 0x0f);
			irq = pci_router->route(PIRQ_GET, 4);
			if (irq & 0x80)
				pci_buf_printf(buf, maxlen, &len, 
						"   PIRQD=disabled\n");
			else
				pci_buf_printf(buf, maxlen, &len, 
						"   PIRQD=%d\n", irq & 0x0f);
		}
	}

	pci_format_bus_tree(&pci_root, buf, maxlen, &len, 2);

	if (len >= maxlen)
		len = maxlen - 1;
	buf[len] = 0;
	return len;
}


int
pci(int mode,u8_t bus,u8_t devfn,u8_t reg,u32_t value,int sz)
{
	u32_t	addr;
	u32_t	val;
	int	n;

	if (sz != 1 && sz != 2 && sz != 4) return -1;

	n=((int)reg%sizeof(u32_t))<<3;
	reg &= ~(sizeof(u32_t)-1);
			
	addr = PCI_ADDR(bus,devfn)|reg;
	outl(CFG_ADDR,addr);

	switch(mode) {
	case PCI_RD:
		if (value == 0) return -1;
		val=inl(CFG_DATA);
		switch(sz) {
		case 1:
			*(u8_t *)(ptrsize_t)value=(u8_t)((val>>n)&0xff);
			break;
		case 2:
			*(u16_t *)(ptrsize_t)value=(u16_t)((val>>n)&0xffff);
			break;
		case 4:
			*(u32_t *)(ptrsize_t)value=val;
			break;
		}
		break;

	case PCI_WR:
		val=inl(CFG_DATA);
		switch(sz) {
		case 1:
			val &= ~(0x000000ffU<<n);
			val |= (value & 0x000000ffU)<<n;
			break;
		case 2:
			val &= ~(0x0000ffffU<<n);
			val |= (value & 0x0000ffffU)<<n;
			break;
		case 4:
			val = value;
			break;
		}
		outl(CFG_ADDR,addr);
		outl(CFG_DATA,val);
		break;
	default:
		return -1;
	}
	return 0;
}

int
pci_ffs(int mask)
{
	int 	bit;

	if (mask==0) return 0;
	for(bit=1;!(mask&1); bit++)
		mask=(uint_t)mask>>1;
	return bit;
}

char 
IntPin(u8_t pin)
{
	switch(pin)
	{
	case 0: return '-';
	case 1: return 'A';
	case 2: return 'B';
	case 3: return 'C';
	case 4: return 'D';
	default: return '?';
	}
}


u32_t
barsize(u32_t mask)
{
	if (mask == 0) 
		return 0;
	return mask & (~mask + 1);
}


void
pci_enable_bridge(pcidev_t *dev)
{
	pcidev_t *bridge;
	int	retval;

	bridge=pci_upstream_bridge(dev);
	if (bridge)
		pci_enable_bridge(bridge);
	if (pci_is_enabled(dev)) {
		if (!dev->is_busmaster)
			pci_set_master(dev);
		return;
	}
	retval=pci_device_enable(dev);
	if (retval)
		printf("Error enabling device %02d:%02d.%d\n",
			dev->bn,
			PCI_SLOT(dev->devfn),
			PCI_FUNC(dev->devfn));
	pci_set_master(dev);
}


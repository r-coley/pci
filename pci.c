int
pci_is_enabled(pcidev_t *dev)
{
	return dev->enable_cnt > 0;
}

void
pci_set_master(pcidev_t *dev)
{
	u16_t	cmd;
	u8_t	lat=0;

	DrvDebug(_PCI,5,"pci_set_master()\n");

	if (dev == 0)
		return;

	if (prcw(dev->bn, dev->devfn, PCI_COMMAND, &cmd) != 0)
		return;

	if (!(cmd & PCI_COMMAND_MASTER)) {
		cmd |= PCI_COMMAND_MASTER;
		if (pwcw(dev->bn, dev->devfn, PCI_COMMAND, cmd) != 0)
			return;
	}

	dev->is_busmaster = 1;

	if (prcb(dev->bn,dev->devfn,PCI_LATENCY_TIMER,&lat) == 0) {
		if (lat < 16) {
			lat=64;	
			pwcb(dev->bn,dev->devfn,PCI_LATENCY_TIMER,lat);
		}
	}
	return;
}

pcidev_t *
pci_physfn(pcidev_t *dev)
{
	/* if (dev->is_virtfn)
		dev=dev->physfn;*/
	return dev;
}

pcidev_t *
pci_upstream_bridge(pcidev_t *dev)
{
	dev=pci_physfn(dev);
	if (pci_is_root_bus(dev->bus))
		return NULL;
	return dev->bus->self;
}

void
pci_enable_bridge(pcidev_t *dev)
{
	pcidev_t *bridge;
	int	retval;

	DrvDebug(_PCI,5,"pci_enable_bridge()\n");

	bridge=pci_upstream_bridge(dev);
	if (bridge)
		pci_enable_bridge(bridge);
	if (pci_is_enabled(dev)) {
		if (!dev->is_busmaster)
			pci_set_master(dev);
		return;
	}
	retval=pci_device_enable(dev);
	if (retval)
		printf("Error enabling device %02d:%02d.%d\n",
			dev->bn,
			PCI_SLOT(dev->devfn),
			PCI_FUNC(dev->devfn));
	pci_set_master(dev);
}

int
pci_is_root_bus(pcibus_t *bus)
{
	return !(bus->parent);
}

int
pci_is_bridge(pcidev_t *dev)
{
	return dev->hdr_type == PCI_HEADER_TYPE_BRIDGE ||
	       dev->hdr_type == PCI_HEADER_TYPE_CARDBUS;
}

int
do_pci_device_enable(pcidev_t *dev,int bars)
{
	u16_t	cmd, newcmd;
	int	i;

	DrvDebug(_PCI,5,"do_pci_device_enable()\n");

	if (dev == 0)
		return -EINVAL;

	if (prcw(dev->bn, dev->devfn, PCI_COMMAND, &cmd) != 0)
		return -EIO;

	newcmd = cmd;

	for (i = 0; i < PCI_ROM_RESOURCE; i++) {
		if (!(bars & (1 << i)))
			continue;
		if (dev->resource[i].flags & IORESOURCE_IO)
			newcmd |= PCI_COMMAND_IO;
		if (dev->resource[i].flags & IORESOURCE_MEM)
			newcmd |= PCI_COMMAND_MEMORY;
	}

	if (dev->master)
		newcmd |= PCI_COMMAND_MASTER;

	if (newcmd == cmd) {
		if (newcmd & PCI_COMMAND_MASTER)
			dev->is_busmaster = 1;
		return 0;
	}

	if (pwcw(dev->bn, dev->devfn, PCI_COMMAND, newcmd) != 0)
		return -EIO;

	if (newcmd & PCI_COMMAND_MASTER)
		dev->is_busmaster = 1;

	return 0;
}

int
pci_device_disable(pcidev_t *dev)
{
	u16_t	cmd;

	DrvDebug(_PCI,5,"pci_device_disable()\n");

	if (dev == 0)
		return -EINVAL;

	if (dev->enable_cnt <= 0) {
		dev->enable_cnt = 0;
		return 0;
	}

	dev->enable_cnt--;
	if (dev->enable_cnt > 0)
		return 0;

	if (prcw(dev->bn, dev->devfn, PCI_COMMAND, &cmd) != 0)
		return -EIO;

	cmd &= ~(PCI_COMMAND_IO | PCI_COMMAND_MEMORY | PCI_COMMAND_MASTER);

	if (pwcw(dev->bn, dev->devfn, PCI_COMMAND, cmd) != 0)
		return -EIO;

	dev->is_busmaster = 0;
	return 0;
}

int
pci_device_enable_flags(pcidev_t *dev,u32_t flags)
{
	pcidev_t *bridge;
	int	err, i, bars=0;

	DrvDebug(_PCI,5,"pci_device_enable_flags()\n");

	if (dev->pm_cap) {
		u16_t 	pmcsr;

		if (prcw(dev->bus->number,
			      dev->devfn,
			      dev->pm_cap+PCI_PM_CTRL,
			      &pmcsr) == 0)
			dev->current_state=(pmcsr & PCI_PM_CTRL_STATE_MASK);
	}
	if (dev->enable_cnt > 0) {
		dev->enable_cnt++;
		return 0;
	}

	dev->enable_cnt++;

	bridge=pci_upstream_bridge(dev);
	if (bridge)
		pci_enable_bridge(bridge);

	for (i=0;i<PCI_ROM_RESOURCE;i++)
		if (dev->resource[i].flags & flags)
			bars |= (1 << i);

	err=do_pci_device_enable(dev,bars);
	if (err<0)
		dev->enable_cnt=0;
	return err;
}

int
pci_device_enable(pcidev_t *dev)
{
	return pci_device_enable_flags(dev,IORESOURCE_MEM|IORESOURCE_IO);
}


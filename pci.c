int
pci(int mode,u8_t bus,u8_t devfn,u8_t reg,u32_t value,int sz)
{
	u32_t	addr;
	u32_t	val;
	int	n;

	if (sz != 1 && sz != 2 && sz != 4) return -1;

	n=((int)reg%sizeof(u32_t))<<3;
	reg &= ~(sizeof(u32_t)-1);
			
	addr = PCI_ADDR(bus,devfn)|reg;
	outl(CFG_ADDR,addr);

	switch(mode) {
	case PCI_RD:
		if (value == 0) return -1;
		val=inl(CFG_DATA);
		switch(sz) {
		case 1:
			*(u8_t *)(ptrsize_t)value=(u8_t)((val>>n)&0xff);
			break;
		case 2:
			*(u16_t *)(ptrsize_t)value=(u16_t)((val>>n)&0xffff);
			break;
		case 4:
			*(u32_t *)(ptrsize_t)value=val;
			break;
		}
		break;

	case PCI_WR:
		val=inl(CFG_DATA);
		switch(sz) {
		case 1:
			val &= ~(0x000000ffU<<n);
			val |= (value & 0x000000ffU)<<n;
			break;
		case 2:
			val &= ~(0x0000ffffU<<n);
			val |= (value & 0x0000ffffU)<<n;
			break;
		case 4:
			val = value;
			break;
		}
		outl(CFG_ADDR,addr);
		outl(CFG_DATA,val);
		break;
	default:
		return -1;
	}
	return 0;
}

int
ffs(int mask)
{
	int bit;
	if (mask==0) return 0;
	for(bit=1;!(mask&1); bit++)
		mask=(uint_t)mask>>1;
	return bit;
}


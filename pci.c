int
get_num_bars(u8_t hdrtype)
{
	switch(hdrtype & 0x7f) {
	case 0: return 6;
	case 1: return 2;
	case 2: return 1;
	}
	return 0;
}

u32_t
get_map_base(u32_t addr) 
{
	if  (addr & 0x01)
		return addr & ~0x03;
	else
		return addr & ~0x0f;
}

static void
pci_print_one_device(pcidev_t *dev)
{
	int	i;
	u8_t	base, sub, prog;
	char	*vendor_name;
	char	*device_name;
	char	*class_name;
	char	*subclass_name;

	DrvDebug(_PCI,5,"pci_print_one_device()\n");

	base = (u8_t)((dev->class >> 16) & 0xff);
	sub  = (u8_t)((dev->class >> 8) & 0xff);
	prog = (u8_t)(dev->class & 0xff);

	vendor_name = get_pci_vendor_name(dev->vendor);
	device_name = get_pci_dev(dev->vendor, dev->device);
	class_name = get_pci_class_name(base);
	subclass_name = get_pci_subclass_name(base, sub);

	printf("PCI: %02x:%02x.%x ",
		dev->bn,
		PCI_SLOT(dev->devfn),
		PCI_FUNC(dev->devfn));

	if (device_name != 0)
		printf("%s", device_name);
	else if (subclass_name != 0)
		printf("%s", subclass_name);
	else if (class_name != 0)
		printf("%s", class_name);
	else
		printf("Unknown PCI Device");

	printf(" [%04x:%04x]", dev->vendor, dev->device);
	if (vendor_name != 0)
		printf(" (%s)", vendor_name);
	printf(" class=%02x/%02x/%02x", base, sub, prog);
	if (dev->intpin != 0 || dev->irq != PCI_INVALID_IRQ)
		printf(" IRQ:%d INT#%c", dev->irq, IntPin(dev->intpin));
	printf("\n");

	for (i = 0; i < DEVICE_COUNT_RESOURCE; i++) {
		if (dev->resource[i].flags == 0)
			continue;

		printf("  BAR%d ", i);
		if (dev->resource[i].flags & IORESOURCE_IO)
			printf("IO  %08lx-%08lx",
				(unsigned long)dev->resource[i].start,
				(unsigned long)dev->resource[i].end);
		else if (dev->resource[i].flags & IORESOURCE_MEM)
			printf("MEM %08lx-%08lx",
				(unsigned long)dev->resource[i].start,
				(unsigned long)dev->resource[i].end);
		else
			printf("??? %08lx-%08lx",
				(unsigned long)dev->resource[i].start,
				(unsigned long)dev->resource[i].end);

		printf("\n");
	}
}

void
pci_print_devices(void)
{
	pcidev_t *dev;

	DrvDebug(_PCI,5,"pci_print_devices()\n");

	if (pci_devices == 0) {
		printf("PCI: no devices enumerated\n");
		return;
	}

	printf("PCI: enumerated devices\n");
	for (dev = pci_devices; dev != 0; dev = dev->next)
		pci_print_one_device(dev);
	printf("\n");
}

u32_t
scan_bus(pcibus_t *bus)
{
	pcidev_t	*dev;
	pcibus_t	*child;
	u32_t		l, iostart, iosize, buses;
	u16_t		cmd, tmp, vid, did, ssvid, ssdid, cr;
	u32_t		class;
	u8_t		bn, max, hdr, mf, rev;
	u8_t		master, irq, intpin;
	u8_t		devfn;
	int		i, slot, fn, num_bars, nfuncs;
	resource_t	res[DEVICE_COUNT_RESOURCE];

	DrvDebug(_PCI,5,"scan_bus()\n");

	max = bus->secondary;
	bn  = bus->number;

	for (slot = 0; slot < 32; slot++)
	{
		hdr = vid = did = 0;
		devfn = DEVFN(slot, 0);

		prcw(bn, devfn, PCI_VENDOR_ID, &vid);
		if (vid == 0xffff || vid == 0x0000)
			continue;

		/* Read header type from function 0 to determine
		 * whether this is a multi-function device. */
		prcb(bn, devfn, PCI_HEADER_TYPE, &hdr);
		if (hdr == 0xff)
			continue;		/* slot not populated */

		mf    = (hdr & 0x80) ? 1 : 0;
		hdr  &= ~0x80;
		nfuncs = mf ? 8 : 1;

		for (fn = 0; fn < nfuncs; fn++)
		{
			devfn = DEVFN(slot, fn);

			/* For fn > 0 re-read vendor to skip unpopulated
			 * functions in a multi-function device. */
			if (fn > 0) {
				vid = 0;
				prcw(bn, devfn, PCI_VENDOR_ID, &vid);
				if (vid == 0xffff || vid == 0x0000)
					continue;
			}

			prcw(bn, devfn, PCI_DEVICE_ID, &did);
			if (did == 0xffff)
				continue;

			/* Re-read header type for each function it can
			 * differ across functions of the same device. */
			if (fn > 0) {
				hdr = 0;
				prcb(bn, devfn, PCI_HEADER_TYPE, &hdr);
				if (hdr == 0xff)
					continue;
				hdr &= ~0x80;
			}

			num_bars = get_num_bars(hdr);

			/* Subsystem vendor is at 0x2c, subsystem ID at 0x2e.
			 * These registers are only valid for header type 0. */
			ssvid = 0;
			ssdid = 0;
			if ((hdr & 0x7f) == 0) {
				prcw(bn, devfn, PCI_SUBSYSTEM_VENDOR_ID, &ssvid);
				prcw(bn, devfn, PCI_SUBSYSTEM_ID,        &ssdid);
			}

			/* Class (24 bits) and revision (8 bits) share one
			 * DWORD at offset 0x08. */
			l   = 0;
			prcd(bn, devfn, PCI_CLASS_REVISION, &l);
			rev   = (u8_t)(l & 0xff);
			class = (l >> 8) & 0x00ffffffU;

			/* BAR probing: size each BAR with decode disabled and always
			 * restore the original contents before leaving the probe path.
			 * Also consume the upper dword of 64-bit memory BARs so later
			 * BARs are not mis-parsed. */
			bzero(res, sizeof(res));
			for (i = 0; i < num_bars; i++)
			{
				u32_t bar_reg = PCI_BASE_ADDRESS_0 + (i * 4);
				u32_t size;
				u32_t orig_bar, probe_bar;
				u32_t orig_bar_hi, probe_bar_hi;
				u16_t probe_cmd, restore_cmd;
				int wrote_probe, wrote_probe_hi, is64;

				iosize = 0;
				orig_bar = 0;
				probe_bar = 0;
				orig_bar_hi = 0;
				probe_bar_hi = 0;
				probe_cmd = 0;
				restore_cmd = 0;
				wrote_probe = 0;
				wrote_probe_hi = 0;
				is64 = 0;
				res[i].start = 0;
				res[i].end   = 0;
				res[i].flags = 0;

				if (prcd(bn, devfn, bar_reg, &orig_bar) != 0)
					continue;

				iostart = orig_bar;
				if (iostart == 0 || iostart == 0xffffffff)
					continue;

				if (!(iostart & PCI_BASE_ADDRESS_SPACE_IO) &&
				    ((iostart & PCI_BASE_ADDRESS_MEM_TYPE_MASK) ==
				     PCI_BASE_ADDRESS_MEM_TYPE_64)) {
					is64 = 1;
					if ((i + 1) >= num_bars)
						continue;
					if (prcd(bn, devfn, bar_reg + 4, &orig_bar_hi) != 0)
						continue;
					if (orig_bar_hi != 0)
						printf("PCI: %02x:%02x.%x BAR%d 64-bit base truncated to 32 bits\n",
							bn, PCI_SLOT(devfn), PCI_FUNC(devfn), i);
					res[i + 1].start = 0;
					res[i + 1].end   = 0;
					res[i + 1].flags = 0;
				}

				if (prcw(bn, devfn, PCI_COMMAND, &probe_cmd) != 0)
					continue;
				restore_cmd = probe_cmd;
				probe_cmd &= ~(PCI_COMMAND_IO | PCI_COMMAND_MEMORY);
				if (pwcw(bn, devfn, PCI_COMMAND, probe_cmd) != 0)
					continue;

				if (pwcd(bn, devfn, bar_reg, 0xffffffffU) == 0) {
					wrote_probe = 1;
					if (prcd(bn, devfn, bar_reg, &probe_bar) == 0)
						iosize = probe_bar;
					if (is64 && pwcd(bn, devfn, bar_reg + 4, 0xffffffffU) == 0) {
						wrote_probe_hi = 1;
						(void)prcd(bn, devfn, bar_reg + 4, &probe_bar_hi);
					}
				}

				if (wrote_probe_hi)
					(void)pwcd(bn, devfn, bar_reg + 4, orig_bar_hi);
				if (wrote_probe)
					(void)pwcd(bn, devfn, bar_reg, orig_bar);
				(void)pwcw(bn, devfn, PCI_COMMAND, restore_cmd);

				if (!wrote_probe || iosize == 0 || iosize == 0xffffffff)
					continue;
				if (is64 && probe_bar_hi != 0 && probe_bar_hi != 0xffffffff)
					printf("PCI: %02x:%02x.%x BAR%d 64-bit size truncated to 32 bits\n",
						bn, PCI_SLOT(devfn), PCI_FUNC(devfn), i);

				res[i].start = get_map_base(iostart);
				if (iostart & PCI_BASE_ADDRESS_SPACE_IO) {
					size = barsize(iosize & PCI_BASE_ADDRESS_IO_MASK);
					res[i].flags = IORESOURCE_IO;
				} else {
					size = barsize(iosize & PCI_BASE_ADDRESS_MEM_MASK);
					res[i].flags = IORESOURCE_MEM;
				}
				res[i].end = size ? (res[i].start + size - 1) : 0;

				if (is64)
					i++;
			}

			/* Non-destructively test whether device supports
			 * bus mastering. */
			master = 0;
			if ((prcw(bn, devfn, PCI_COMMAND, &cmd) == 0) &&
			    (pwcw(bn, devfn, PCI_COMMAND, (cmd | PCI_COMMAND_MASTER)) == 0) &&
			    (prcw(bn, devfn, PCI_COMMAND, &tmp) == 0))
			{
				master = ((tmp & PCI_COMMAND_MASTER) != 0) ? 1 : 0;
				pwcw(bn, devfn, PCI_COMMAND, cmd);
			}

			/* Read IRQ line and interrupt pin.*/
			irq    = 0;
			intpin = 0;
			if (prcb(bn, devfn, PCI_INTERRUPT_LINE, &irq) != 0) continue;
			if (prcb(bn, devfn, PCI_INTERRUPT_PIN,  &intpin) != 0) continue;
			if (intpin > 4) intpin=0;

			/* Allocate device and info structures.
			 * Bug fixed: original allocated dev then immediately
			 * tested dev->info in the same if-condition, but if
			 * dev allocation succeeded and info allocation failed
			 * the already-allocated dev was leaked. Fix: allocate
			 * and test separately. */
			dev = pci_malloc(sizeof(pcidev_t));
			if (dev == 0)
				continue;

			dev->info = pci_malloc(sizeof(pcidevinfo_t));
			if (dev->info == 0) {
				kmem_free(dev, sizeof(pcidev_t));
				continue;
			}

			dev->bus              = bus;
			dev->bn               = bus->number;
			dev->devfn            = devfn;
			dev->vendor           = vid;
			dev->device           = did;
			dev->subsystem_vendor = ssvid;
			dev->subsystem_device = ssdid;
			dev->revision         = rev;
			dev->class            = class;
			dev->hdr_type         = hdr;
			dev->master           = master;
			dev->irq              = irq;
			dev->intpin           = intpin;
			bcopy(&res, &dev->resource, sizeof(res));

			dev->info->vendor = vid;
			dev->info->device = did;
			dev->info->name   = "Unknown PCI Device";

			/* Prepend to global device list and bus device list. */
			dev->next    = pci_devices;
			pci_devices  = dev;
			dev->sibling = bus->devices;
			bus->devices = dev;

			/* If this is a PCI-to-PCI bridge, allocate a child
			 * bus structure, assign tentative bus numbers, recurse
			 * to scan subordinate buses, then write the final
			 * subordinate number back. */
			if (dev->class == PCI_CLASS_BRIDGE_PCI)
			{
				child = kmem_zalloc(sizeof(pcibus_t), KM_NOSLEEP);
				if (child == 0)
					continue;

				child->self        = dev;
				child->parent      = bus;
				child->number      = ++max;
				child->secondary   = max;
				child->primary     = bus->number;
				child->subordinate = 0xff;

				child->next  = bus->children;
				bus->children = child;

				/* Disable the bridge while we configure it and always
				 * restore the original command register on exit. */
				if (prcw(bn, devfn, PCI_COMMAND, &cr) == 0) {
					u32_t orig_buses = 0;
					int bridge_cfg_ok = 1;

					if (pwcw(bn, devfn, PCI_COMMAND, 0x0000) != 0)
						bridge_cfg_ok = 0;
					if (bridge_cfg_ok && pwcw(bn, devfn, PCI_STATUS, 0xffff) != 0)
						bridge_cfg_ok = 0;
					if (bridge_cfg_ok && prcd(bn, devfn, 0x18, &orig_buses) != 0)
						bridge_cfg_ok = 0;

					buses = orig_buses;
					if (bridge_cfg_ok) {
						buses &= 0xff000000U;
						buses |= ((u32_t)child->primary     <<  0)
						       | ((u32_t)child->secondary   <<  8)
						       | ((u32_t)child->subordinate << 16);
						if (pwcd(bn, devfn, 0x18, buses) != 0)
							bridge_cfg_ok = 0;
					}

					if (bridge_cfg_ok) {
						/* Recurse — fills in child->subordinate. */
						max = scan_bus(child);

						/* Write the real subordinate number back. */
						child->subordinate = max;
						buses = (buses & 0xff00ffffU)
						      | ((u32_t)max << 16);
						if (pwcd(bn, devfn, 0x18, buses) != 0) {
							bridge_cfg_ok = 0;
							(void)pwcd(bn, devfn, 0x18, orig_buses);
						}
					}

					/* Re-enable the bridge. */
					(void)pwcw(bn, devfn, PCI_COMMAND, cr);
					if (!bridge_cfg_ok)
						printf("PCI: %02x:%02x.%x bridge configuration incomplete\n",
							bn, PCI_SLOT(devfn), PCI_FUNC(devfn));
				}
			}
		}
	}
	return max;
}

static pcidevid_t *
pci_match_one_device(pcidev_t *dev,pcidevid_t *id)
{
	if ((id->vendor == PCI_ANY_ID || id->vendor == dev->vendor) &&
	    (id->device == PCI_ANY_ID || id->device == dev->device) &&
	    (id->subvendor == PCI_ANY_ID || 
	     id->subvendor == dev->subsystem_vendor) &&
	    (id->subdevice == PCI_ANY_ID || 
	     id->subdevice == dev->subsystem_device) &&
	    !((id->class ^ dev->class) & id->class_mask))
		return id;
	return 0;
}

pcidev_t *
find_pcidev(u8_t bus,u8_t devfn)
{
	pcidev_t *dev;

	DrvDebug(_PCI,5,"find_pcidev()\n");

	/*** For each pci_devices */
	for(dev=pci_devices; dev; dev=dev->next)
	{
		/* printf("dev %02x.%02x.%x [%04x,%04x]\n",
			dev->bn,dev->devfn,
			dev->vendor,dev->device);*/
		if (dev->bn == bus &&
		    dev->devfn == devfn)
			return dev; 
	}
	return (pcidev_t *)0;
}

/*
 * called with (struct pci_driver *)&pcnet_driver
 */
int
pci_register_driver(struct pci_driver *drv)
{
	pcidev_t *dev;
	pcidevid_t *id;
	int	count=0;

	DrvDebug(_PCI,5,"pci_register_driver(%s)\n",drv ? drv->name : "??");

	if (!drv || !drv->id_table)
		return -EINVAL;

	/*** For each pci_devices */
	for(dev=pci_devices; dev; dev=dev->next)
	{
		/*
		printf("dev %x %02x.%02x.%x [%04x,%04x]\n",
			dev,
			dev->bn,PCI_SLOT(dev->devfn),PCI_FUNC(dev->devfn),
			dev->vendor,dev->device); */

		for(id=drv->id_table; id->vendor; id++)
		{
			if (pci_match_one_device(dev,id) != 0) {
				int ret=0;

				if (drv->probe)
					ret = (*drv->probe)(dev,id);

				if (ret == 0) {
					dev->info->name=drv->name;
					count++;
					break;
				}
			}
		}
	}
	return count;
}

